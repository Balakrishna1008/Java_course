package com.strings;

public class Math_method {

	public static void main(String[] args) {
		
		System.out.println(Math.max(2,10));
		System.out.println(Math.min(2, 10));
		System.out.println(Math.pow(12,10));
		System.out.println(Math.sqrt(23));
		System.out.println(Math.abs(-16.33));
		System.out.println(Math.ceil(16.21));
		System.out.println(Math.floor(16.11));
		System.out.println(Math.random());

		
	}

}
